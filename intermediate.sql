-- Query from both the tables utilizing Joins
SELECT E.EMPFNAME, E.EMPLNAME, E.DEPTCODE,
       D.DEPTNAME, D.LOCATION
       FROM EMPLOYEE E, DEPARTMENT D
       WHERE E.DEPTCODE = D.DEPTCODE
       ORDER BY E.EMPFNAME, D.LOCATION;
      

SELECT EMPCODE, EMPFNAME, SUM(COMMISSION+SALARY) AS "TOTAL SALARY"
FROM EMPLOYEE
GROUP BY EMPCODE, EMPFNAME;

